/// AI出力のLaTeXから危険・要注意コマンドを検出して警告を作る
pub fn scan_latex_security(latex: &str) -> Vec<AiWarning> {
    let mut warnings = vec![];
    let lower = latex.to_ascii_lowercase();
    let patterns: &[(&str, &str, &str)] = &[
        ("\\write18", "シェルコマンド実行の記述が含まれています", "error"),
        ("\\input{", "外部ファイル読み込み（\\input）が含まれています", "error"),
        ("\\include{", "外部ファイル読み込み（\\include）が含まれています", "error"),
        ("\\openout", "ファイル書き込みの記述が含まれています", "error"),
        ("\\openin", "ファイル読み込みの記述が含まれています", "error"),
        ("\\catcode", "catcode変更が含まれています", "error"),
        ("\\usepackage", "本文内の\\usepackageはテンプレート側で管理してください", "warning"),
        ("\\documentclass", "\\documentclassが含まれています。本文断片だけを使用してください", "error"),
    ];
    for (pat, msg, severity) in patterns {
        if lower.contains(pat) {
            warnings.push(AiWarning {
                code: "SUSPICIOUS_COMMAND".into(),
                severity: (*severity).into(),
                message: format!("{}: {}", pat, msg),
            });
        }
    }
    // 絶対パスや親ディレクトリ参照のincludegraphics
    let mut rest = latex;
    while let Some(pos) = rest.find("\\includegraphics") {
        let after = &rest[pos..];
        if let Some(open) = after.find('{') {
            if let Some(close) = after[open..].find('}') {
                let arg = &after[open + 1..open + close];
                if arg.contains(':') || arg.starts_with('/') || arg.starts_with('\\') || arg.contains("..") {
                    warnings.push(AiWarning {
                        code: "UNSAFE_IMAGE_PATH".into(),
                        severity: "error".into(),
                        message: format!("画像参照 {} は教材アセット外のパスです。挿入前に修正してください", arg),
                    });
                }
                rest = &after[open + close..];
                continue;
            }
        }
        rest = &after[1..];
    }
    warnings
}

/// AI生成の解答・解説が指定された本文幅を壊す典型的な記述を検出する。
pub fn scan_solution_layout(latex: &str, solution_layout: &str) -> Vec<AiWarning> {
    let two_column = solution_layout != "single_column";
    let mut warnings = vec![];
    let forbidden = [
        ("\\textwidth", "\\textwidthではなく\\linewidthを使用してください"),
        ("\\begin{multicols}", "解答本文の外側で多段組を追加しないでください"),
        ("\\twocolumn", "解答本文の外側で二段組を追加しないでください"),
        ("\\begin{columns}", "columns環境は解答冊子の段組と衝突します"),
        ("\\begin{minipage}", "固定幅のminipageは指定された本文幅からはみ出す可能性があります"),
        ("\\begin{center}", "図はcenter環境を使わず左寄せで配置してください"),
        ("\\centering", "図は\\centeringを使わず左寄せで配置してください"),
        ("\\begin{figure}", "図はfigureフロートを使わず本文位置へ配置してください"),
        ("\\begin{figure*}", "figure*は本文断片の幅を超えるため使用できません"),
    ];
    for (pattern, message) in forbidden {
        if latex.contains(pattern) {
            warnings.push(AiWarning {
                code: "TWO_COLUMN_LAYOUT".into(),
                severity: "error".into(),
                message: message.into(),
            });
        }
    }

    let mut rest = latex;
    while let Some(pos) = rest.find("\\includegraphics") {
        let after_command = &rest[pos + "\\includegraphics".len()..];
        let trimmed = after_command.trim_start();
        let options = if let Some(value) = trimmed.strip_prefix('[') {
            value.find(']').map(|end| &value[..end])
        } else {
            None
        };
        match options {
            None => warnings.push(AiWarning {
                code: "FIGURE_SIZE".into(),
                severity: "error".into(),
                message: "図には列幅基準のwidthを指定してください（標準は0.65\\linewidth）".into(),
            }),
            Some(options) => {
                let compact: String = options.chars().filter(|c| !c.is_whitespace()).collect();
                let width = compact
                    .split(',')
                    .find_map(|part| part.strip_prefix("width="))
                    .map(|value| value.trim_matches(|c| c == '{' || c == '}'));
                match width {
                    None => warnings.push(AiWarning {
                        code: "FIGURE_SIZE".into(),
                        severity: "error".into(),
                        message: "図にはwidth=0.45〜0.80\\linewidthの範囲で幅を指定してください".into(),
                    }),
                    Some("\\linewidth") => warnings.push(AiWarning {
                        code: "FIGURE_SIZE".into(),
                        severity: "error".into(),
                        message: "図を列幅いっぱいにせず、自然な大きさ（標準0.65\\linewidth）にしてください".into(),
                    }),
                    Some(value) => {
                        if let Some(number) = value.strip_suffix("\\linewidth") {
                            let valid = number
                                .parse::<f64>()
                                .is_ok_and(|ratio| (0.1..=0.85).contains(&ratio));
                            if !valid {
                                warnings.push(AiWarning {
                                    code: "FIGURE_SIZE".into(),
                                    severity: "error".into(),
                                    message: "図の幅は0.10〜0.85\\linewidthの範囲にしてください".into(),
                                });
                            }
                        } else if let Some(number) = value.strip_suffix("cm") {
                            let max_width_cm = if two_column { 7.0 } else { 15.0 };
                            let valid = number
                                .parse::<f64>()
                                .is_ok_and(|width_cm| (0.1..=max_width_cm).contains(&width_cm));
                            if !valid {
                                warnings.push(AiWarning {
                                    code: "FIGURE_SIZE".into(),
                                    severity: "error".into(),
                                    message: format!(
                                        "固定幅は{}cm以下にするか、\\linewidth基準にしてください",
                                        max_width_cm
                                    ),
                                });
                            }
                        } else {
                            warnings.push(AiWarning {
                                code: "FIGURE_SIZE".into(),
                                severity: "error".into(),
                                message: format!(
                                    "図の幅は\\linewidth基準（推奨0.65）または{}cm以下で指定してください",
                                    if two_column { 7.0 } else { 15.0 }
                                ),
                            });
                        }
                    }
                }
                if compact.contains("height=") && !compact.contains("keepaspectratio") {
                    warnings.push(AiWarning {
                        code: "FIGURE_ASPECT_RATIO".into(),
                        severity: "error".into(),
                        message: "幅と高さを指定する図にはkeepaspectratioを付けてください".into(),
                    });
                }
            }
        }
        rest = after_command;
    }
    warnings
}

fn contains_latex_command(text: &str, command: &str) -> bool {
    let mut search_from = 0;
    while let Some(relative) = text[search_from..].find(command) {
        let end = search_from + relative + command.len();
        let is_command_end = match text[end..].chars().next() {
            Some(next) => !next.is_ascii_alphabetic(),
            None => true,
        };
        if is_command_end {
            return true;
        }
        search_from = end;
    }
    false
}

/// 入力テキストが軌跡・領域を求める問題かを判定する。
/// 特定の数式や例題には依存せず、問題種別を表す語だけを見る。
pub fn is_trajectory_region_problem(text: &str) -> bool {
    text.contains("軌跡")
        || text.contains("領域")
        || text.contains("点の存在範囲")
        || text.contains("点の動く範囲")
        || ((text.contains("動点") || text.contains("点が動く") || text.contains("点の動く"))
            && (text.contains("範囲") || text.contains("図形") || text.contains("通過する部分")))
        || ((text.contains("線分") || text.contains("図形") || text.contains("曲線族"))
            && (text.contains("通過する部分")
                || text.contains("通過する領域")
                || text.contains("覆う部分")
                || text.contains("覆う領域")
                || text.contains("包絡線")))
}

/// 点ではなく、動く線分・図形・曲線族が通過または被覆する領域を扱う問題か。
pub fn is_moving_figure_region_problem(text: &str) -> bool {
    let moving_figure = ["線分", "直線", "円", "図形", "曲線", "曲線族", "直線群"]
        .iter()
        .any(|word| text.contains(word));
    let swept_region = [
        "通過する部分",
        "通過する領域",
        "覆う部分",
        "覆う領域",
        "動いてできる領域",
        "掃過",
        "包絡線",
    ]
    .iter()
    .any(|word| text.contains(word));
    moving_figure && swept_region
}

/// 動く線分の通過領域で、補間パラメータによる所属条件を自然に使える問題か。
/// 線分では端点の凸結合により線分上の条件を正確に保持できるため、原則として優先する。
pub fn prefers_swept_region_membership_structure(text: &str) -> bool {
    is_moving_figure_region_problem(text) && text.contains("線分")
}

/// 軌跡・領域の決定が途中段階で、その後に別の量を求める複合問題か。
pub fn is_compound_trajectory_region_problem(text: &str) -> bool {
    if !is_trajectory_region_problem(text) {
        return false;
    }
    let ask_position = [text.rfind("求め"), text.rfind("計算")]
        .into_iter()
        .flatten()
        .max()
        .unwrap_or(text.len());
    let before_final_ask = &text[..ask_position];
    [
        "体積",
        "面積",
        "確率",
        "最大値",
        "最小値",
        "最大・最小",
        "軌跡",
        "領域",
    ]
    .iter()
    .filter_map(|word| before_final_ask.rfind(word).map(|position| (position, *word)))
    .max_by_key(|(position, _)| *position)
    .is_some_and(|(_, final_target)| {
        matches!(
            final_target,
            "体積" | "面積" | "確率" | "最大値" | "最小値" | "最大・最小"
        )
    })
}

/// P(x,y)\in R から始まる厳密な一続きの同値変形を必須とする問題か。
/// 点の軌跡・点の存在範囲が最終目的の場合だけtrueにする。
pub fn requires_strict_point_locus_structure(text: &str) -> bool {
    let explicitly_targets_a_point = trajectory_target_point_name(text).is_some()
        || [
            "点の軌跡",
            "点の存在範囲",
            "点の動く範囲",
            "点が存在する領域",
        ]
        .iter()
        .any(|phrase| text.contains(phrase));
    is_trajectory_region_problem(text)
        && !is_moving_figure_region_problem(text)
        && !is_compound_trajectory_region_problem(text)
        && explicitly_targets_a_point
}

pub fn should_attach_trajectory_instructions(source_type: &str, input_text: &str) -> bool {
    source_type == "image" || is_trajectory_region_problem(input_text)
}

fn normalized_problem_symbols(text: &str) -> String {
    text.chars()
        .filter(|ch| {
            !ch.is_whitespace()
                && !matches!(ch, '$' | '\\' | '{' | '}' | '(' | ')' | '[' | ']')
        })
        .collect()
}

fn problem_defined_region_symbol(text: &str) -> Option<char> {
    let normalized = normalized_problem_symbols(text);
    for marker in ["領域を", "部分を", "範囲を", "軌跡を"] {
        let mut rest = normalized.as_str();
        while let Some(position) = rest.find(marker) {
            let after = &rest[position + marker.len()..];
            if let Some(end) = after.find("とする") {
                if let Some(symbol) = after[..end]
                    .chars()
                    .find(|ch| ch.is_ascii_uppercase())
                {
                    return Some(symbol);
                }
            }
            if after.is_empty() {
                break;
            }
            rest = after;
        }
    }
    None
}

fn swept_region_auxiliary_point_name(text: &str) -> char {
    let symbols = normalized_problem_symbols(text);
    ['X', 'Y', 'Z', 'U', 'W']
        .into_iter()
        .find(|candidate| !symbols.contains(*candidate))
        .unwrap_or('X')
}

/// 問題文ですでに与えられた、軌跡を求める点名を可能な範囲で取り出す。
/// 中点・動点の明示を優先し、その後「点Qの軌跡」の形を調べる。
pub fn trajectory_target_point_name(text: &str) -> Option<char> {
    let normalized = normalized_problem_symbols(text);
    for marker in ["中点", "動点"] {
        if let Some(position) = normalized.find(marker) {
            if let Some(name) = normalized[position + marker.len()..]
                .chars()
                .take(16)
                .find(|ch| ch.is_ascii_uppercase())
            {
                return Some(name);
            }
        }
    }
    for marker in ["の軌跡", "の動く範囲", "の領域"] {
        if let Some(position) = normalized.find(marker) {
            if let Some(name) = normalized[..position]
                .chars()
                .rev()
                .take(16)
                .find(|ch| ch.is_ascii_uppercase())
            {
                return Some(name);
            }
        }
    }
    None
}

fn compact_latex_structure(latex: &str) -> String {
    latex
        .chars()
        .filter(|ch| !ch.is_whitespace() && *ch != '&')
        .collect::<String>()
        .replace("\\left", "")
        .replace("\\right", "")
        .replace("\\bigl", "")
        .replace("\\bigr", "")
        .replace("\\Bigl", "")
        .replace("\\Bigr", "")
}

