fn point_membership_name(compact: &str) -> Option<char> {
    let chars: Vec<char> = compact.chars().collect();
    chars.iter().enumerate().find_map(|(index, ch)| {
        let is_membership = ch.is_ascii_uppercase()
            && chars.get(index + 1) == Some(&'(')
            && chars.get(index + 2) == Some(&'x')
            && chars.get(index + 3) == Some(&',')
            && chars.get(index + 4) == Some(&'y')
            && chars.get(index + 5) == Some(&')')
            && chars.get(index + 6) == Some(&'\\')
            && chars.get(index + 7) == Some(&'i')
            && chars.get(index + 8) == Some(&'n');
        is_membership.then_some(*ch)
    })
}

fn opening_declares_trajectory_coordinate(latex: &str, point_name: char) -> bool {
    let opening_end = [
        latex.find("\\["),
        latex.find("\\begin{align"),
        latex.find("\\begin{gather"),
        latex.find("\\begin{equation"),
    ]
    .into_iter()
    .flatten()
    .min()
    .unwrap_or(latex.len());
    let opening = compact_latex_structure(&latex[..opening_end]).replace('$', "");
    opening.contains(&format!("座標を{}(x,y)とする", point_name))
}

fn push_missing_coordinate_setup_warning(
    warnings: &mut Vec<AiWarning>,
    latex: &str,
    compact: &str,
    point_name: char,
) {
    let point_coordinate = format!("{}(x,y)", point_name);
    if compact.contains(&point_coordinate)
        && !opening_declares_trajectory_coordinate(latex, point_name)
    {
        warnings.push(trajectory_warning(
            "TRAJECTORY_MISSING_COORDINATE_SETUP",
            &format!(
                "後で{}(x,y)を使用するため、必要最小限の準備計算より前に『求める軌跡をRとし、点{}の座標を{}(x,y)とする』等の形で座標を設定してください",
                point_name, point_name, point_name
            ),
        ));
    }
}

