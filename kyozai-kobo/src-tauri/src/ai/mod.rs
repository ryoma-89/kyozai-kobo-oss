fn push_existence_layout_warnings(warnings: &mut Vec<AiWarning>, latex: &str) {
    for phrase in ["満たす実数", "となる実数"] {
        for (position, _) in latex.match_indices(phrase) {
            let after = &latex[position..];
            if !after
                .chars()
                .take(120)
                .collect::<String>()
                .contains("存在する")
            {
                continue;
            }
            let before = &latex[..position];
            let Some(gathered_start) = before.rfind("\\begin{gathered}") else {
                warnings.push(trajectory_warning(
                    "TRAJECTORY_EXISTENCE_LAYOUT",
                    "複数条件の存在条件はgathered内で左波括弧にまとめ、存在を表す文章を下の行へ置いてください",
                ));
                continue;
            };
            let block = &before[gathered_start..];
            let multiple_conditions = block.contains("\\begin{aligned}") || block.contains("\\quad");
            if !multiple_conditions {
                continue;
            }
            let left = block.rfind("\\left\\{");
            let aligned = block.rfind("\\begin{aligned}");
            let aligned_end = block.rfind("\\end{aligned}");
            let right = block.rfind("\\right.");
            let ordered = matches!((left, aligned, aligned_end, right), (Some(a), Some(b), Some(c), Some(d)) if a < b && b < c && c < d);
            let phrase_is_below = right
                .map(|right_position| {
                    block[right_position + "\\right.".len()..].contains("\\\\")
                })
                .unwrap_or(false);
            if !ordered || !phrase_is_below {
                warnings.push(trajectory_warning(
                    "TRAJECTORY_EXISTENCE_LAYOUT",
                    "条件が2つ以上ある存在条件はleft波括弧とalignedでまとめ、『を満たす実数…が存在する』を連立条件の下の行へ置いてください",
                ));
            }
        }
    }
}

fn text_command_contains_math_delimiters(latex: &str) -> bool {
    let mut search_from = 0;
    while let Some(relative_start) = latex[search_from..].find("\\text{") {
        let content_start = search_from + relative_start + "\\text{".len();
        let mut depth = 1usize;
        let mut content_end = None;
        for (relative_index, ch) in latex[content_start..].char_indices() {
            match ch {
                '{' => depth += 1,
                '}' => {
                    depth -= 1;
                    if depth == 0 {
                        content_end = Some(content_start + relative_index);
                        break;
                    }
                }
                _ => {}
            }
        }
        let Some(content_end) = content_end else {
            break;
        };
        let content = &latex[content_start..content_end];
        if content.contains('$')
            || content.contains("\\(")
            || content.contains("\\)")
            || content.contains("\\[")
            || content.contains("\\]")
        {
            return true;
        }
        search_from = content_end + 1;
    }
    false
}

fn braced_system_contains_condition_quotes(latex: &str) -> bool {
    let mut rest = latex;
    while let Some(start) = rest.find("\\left\\{") {
        let after_opening = &rest[start + "\\left\\{".len()..];
        let end = [after_opening.find("\\right."), after_opening.find("\\right\\}")]
            .into_iter()
            .flatten()
            .min()
            .unwrap_or(after_opening.len());
        let block = &after_opening[..end];
        if block.contains("\\begin{aligned}")
            && (block.contains('「') || block.contains('」'))
        {
            return true;
        }
        if end >= after_opening.len() {
            break;
        }
        rest = &after_opening[end..];
    }
    false
}

fn gathered_condition_has_multiple_quote_pairs(latex: &str) -> bool {
    let mut rest = latex;
    while let Some(start) = rest.find("\\begin{gathered}") {
        let after_opening = &rest[start + "\\begin{gathered}".len()..];
        let end = after_opening
            .find("\\end{gathered}")
            .unwrap_or(after_opening.len());
        let block = &after_opening[..end];
        if block.contains("存在する")
            && (block.matches('「').count() > 1 || block.matches('」').count() > 1)
        {
            return true;
        }
        if end >= after_opening.len() {
            break;
        }
        rest = &after_opening[end + "\\end{gathered}".len()..];
    }
    false
}

fn existence_condition_is_fully_quoted(latex: &str) -> bool {
    for (position, _) in latex.match_indices("存在する") {
        let before = &latex[..position];
        let last_open = before.rfind('「');
        let last_close = before.rfind('」');
        let has_active_open = matches!((last_open, last_close), (Some(open), Some(close)) if open > close)
            || matches!((last_open, last_close), (Some(_), None));
        let after = &latex[position + "存在する".len()..];
        let close_is_after = after
            .find('」')
            .is_some_and(|close| close <= 160 && !after[..close].contains('「'));
        if !has_active_open || !close_is_after {
            return false;
        }
    }
    true
}

fn system_existence_condition_is_fully_quoted(latex: &str) -> bool {
    let mut rest = latex;
    while let Some(start) = rest.find("\\begin{gathered}") {
        let after_opening = &rest[start + "\\begin{gathered}".len()..];
        let end = after_opening
            .find("\\end{gathered}")
            .unwrap_or(after_opening.len());
        let block = &after_opening[..end];
        if block.contains("\\left\\{") && block.contains("存在する") {
            let open_quote = block.find('「');
            let left_brace = block.find("\\left\\{");
            let right_brace = block.find("\\right.");
            let existence_text = block.find("を満たす実数");
            let close_quote = block.rfind('」');
            let ordered = matches!(
                (open_quote, left_brace, right_brace, existence_text, close_quote),
                (Some(a), Some(b), Some(c), Some(d), Some(e)) if a < b && b < c && c < d && d < e
            );
            if !ordered
                || block.matches('「').count() != 1
                || block.matches('」').count() != 1
            {
                return false;
            }
        }
        if end >= after_opening.len() {
            break;
        }
        rest = &after_opening[end + "\\end{gathered}".len()..];
    }
    true
}

fn quote_is_followed_by_outside_condition_fragment(latex: &str) -> bool {
    for (position, _) in latex.match_indices('」') {
        let mut tail = latex[position + '」'.len_utf8()..].trim_start();
        if let Some(after_brace) = tail.strip_prefix('}') {
            tail = after_brace.trim_start();
        }
        if tail.starts_with("\\quad")
            && !tail.starts_with("\\quad\\Longleftrightarrow")
            && !tail.starts_with("\\quad\\Longrightarrow")
        {
            return true;
        }
        if tail.starts_with(|ch: char| ch.is_ascii_alphanumeric()) {
            return true;
        }
        if ["\\theta", "\\alpha", "\\beta", "\\gamma", "\\lambda"]
            .iter()
            .any(|variable| tail.starts_with(variable))
        {
            return true;
        }
    }
    false
}

/// 日本語と数式が混在する条件の鉤括弧が、条件全体を1組で囲んでいるかを検査する。
/// 完成答案は書き換えず、違反時は再生成用の構造警告だけを返す。
pub fn scan_condition_quote_structure(latex: &str) -> Vec<AiWarning> {
    let mut warnings = vec![];

    if text_command_contains_math_delimiters(latex) {
        warnings.push(trajectory_warning(
            "CONDITION_QUOTE_MATH_MODE",
            "数式環境内の\\text{}へ$...$、\\(...\\)、\\[...\\]を入れず、日本語だけを\\text{}に入れて数式部分は数式モードのまま記述してください",
        ));
    }

    if braced_system_contains_condition_quotes(latex) {
        warnings.push(trajectory_warning(
            "CONDITION_QUOTE_BRACED_SYSTEM",
            "左波括弧内の各数式を個別の鉤括弧で囲まないでください。連立式と存在文が1つの条件なら、開き鉤括弧を左波括弧より前へ置き、存在文の末尾で閉じて全体を1組で囲んでください",
        ));
    }

    if gathered_condition_has_multiple_quote_pairs(latex) {
        warnings.push(trajectory_warning(
            "CONDITION_QUOTE_MULTIPLE_PAIRS",
            "複数行で論理的に1つの条件は、各行を個別に囲まず、条件全体を先頭の「と末尾の」による1組だけで囲んでください",
        ));
    }

    let partial_opening = ["「を満たす実数", "「となる実数"]
        .iter()
        .any(|pattern| latex.contains(pattern));
    let detached_quote = latex.contains("\\text{「}\\quad")
        || latex.contains("\\quad\\text{」}")
        || quote_is_followed_by_outside_condition_fragment(latex);
    if !existence_condition_is_fully_quoted(latex)
        || !system_existence_condition_is_fully_quoted(latex)
        || partial_opening
        || detached_quote
    {
        warnings.push(trajectory_warning(
            "CONDITION_QUOTE_SCOPE",
            "日本語と数式・点名・変数が組み合わさる1つの条件は、その全体を先頭から末尾まで1組の鉤括弧で囲んでください。連立式と存在文が1つの条件なら、開き鉤括弧を左波括弧より前へ、閉じ鉤括弧を存在文の末尾へ置いてください",
        ));
    }

    warnings
}

fn gathered_existence_blocks(latex: &str) -> Vec<(usize, &str)> {
    let opening = "\\begin{gathered}";
    let closing = "\\end{gathered}";
    let mut blocks = vec![];
    let mut search_start = 0;
    while let Some(relative_start) = latex[search_start..].find(opening) {
        let start = search_start + relative_start;
        let Some(relative_end) = latex[start..].find(closing) else {
            break;
        };
        let end = start + relative_end + closing.len();
        let block = &latex[start..end];
        if block.contains("存在する」") {
            blocks.push((start, block));
        }
        search_start = end;
    }
    blocks
}

fn existence_variable_count(block: &str) -> Option<usize> {
    let marker = "\\text{を満たす実数 }";
    let tail = "\\text{ が存在する」}";
    let start = block.find(marker)? + marker.len();
    let end = block[start..].find(tail)? + start;
    let variables = block[start..end].trim();
    if variables.is_empty() {
        return None;
    }
    Some(variables.split(',').filter(|part| !part.trim().is_empty()).count())
}

/// 軌跡・領域の生成結果を構造面から検査する。
/// 完成答案を正規表現等で書き換えず、違反を警告して再生成または手修正へつなぐ。
pub fn scan_trajectory_solution_structure(problem_text: &str, latex: &str) -> Vec<AiWarning> {
    let mut warnings = vec![];
    let compact = compact_latex_structure(latex);
    let problem_context = if problem_text.trim().is_empty() {
        latex
    } else {
        problem_text
    };
    let strict_point_locus = requires_strict_point_locus_structure(problem_context);
    let prefer_swept_membership = prefers_swept_region_membership_structure(problem_context);
    let membership_equivalence = strict_point_locus || prefer_swept_membership;
    let defined_region_symbol = problem_defined_region_symbol(problem_context);

    if membership_equivalence && !latex.contains("\\Longleftrightarrow") {
        warnings.push(trajectory_warning(
            "TRAJECTORY_MISSING_EQUIVALENCE",
            if strict_point_locus {
                "点の軌跡・存在範囲を求める解答本体は、求める点の集合への所属から始まる一続きの同値変形にしてください"
            } else {
                "動く線分の通過領域は、xy平面上の任意の点の所属を補間パラメータの存在条件で自然に表せるため、その所属条件から同値変形を開始してください"
            },
        ));
    }

    if strict_point_locus {
        if let Some(point_name) = trajectory_target_point_name(problem_context) {
            let point_membership = format!("{}(x,y)\\in", point_name);
            if !compact.contains(&point_membership) {
                warnings.push(trajectory_warning(
                    "TRAJECTORY_POINT_NAME",
                    &format!(
                        "問題文で定義された点{}を保持し、解答本体を{}(x,y)\\in R等の形で開始してください",
                        point_name, point_name
                    ),
                ));
            }

            push_missing_coordinate_setup_warning(&mut warnings, latex, &compact, point_name);
        } else {
            if let Some(point_name) = point_membership_name(&compact) {
                push_missing_coordinate_setup_warning(&mut warnings, latex, &compact, point_name);
            } else {
                warnings.push(trajectory_warning(
                    "TRAJECTORY_POINT_NAME",
                    "求める点をX(x,y)の形で定義し、その点の集合への所属から同値変形を開始してください",
                ));
            }
        }

        let input_uses_r = normalized_problem_symbols(problem_context).contains('R');
        if defined_region_symbol.is_none() && !input_uses_r && !compact.contains("\\inR") {
            warnings.push(trajectory_warning(
                "TRAJECTORY_SET_SYMBOL",
                "問題文で集合記号が定義されていない点の軌跡・存在範囲にはRを使用してください",
            ));
        } else if defined_region_symbol == Some('D')
            && latex.contains("判別式")
            && !compact.contains("\\inR")
        {
            warnings.push(trajectory_warning(
                "TRAJECTORY_SET_SYMBOL",
                "問題文の領域Dと実際に使用する判別式Dが衝突するため、判別式をDのまま維持し、領域側をR等へ変更して冒頭で明示してください",
            ));
        }
    }

    if prefer_swept_membership {
        let point_name = swept_region_auxiliary_point_name(problem_context);
        let actual_discriminant_conflict =
            defined_region_symbol == Some('D') && latex.contains("判別式");
        let region_symbol = if actual_discriminant_conflict {
            'R'
        } else {
            defined_region_symbol.unwrap_or('R')
        };
        let membership = format!("{}(x,y)\\in{}", point_name, region_symbol);
        let membership_starts_equivalence = compact.find(&membership).is_some_and(|position| {
            let after_membership = &compact[position + membership.len()..];
            let equivalence = after_membership.find("\\Longleftrightarrow");
            let implication = after_membership.find("\\Longrightarrow");
            matches!(equivalence, Some(equivalence_position) if implication.map_or(true, |implication_position| equivalence_position < implication_position))
        });
        if !membership_starts_equivalence {
            warnings.push(trajectory_warning(
                "TRAJECTORY_SWEPT_MEMBERSHIP",
                &format!(
                    "xy平面上の任意の点を既存記号と衝突しない{}(x,y)とし、{}(x,y)\\in {}から線分上の存在条件を同値変形してください",
                    point_name, point_name, region_symbol
                ),
            ));
        }
        if !opening_declares_swept_region_point(latex, point_name, region_symbol) {
            warnings.push(trajectory_warning(
                "TRAJECTORY_SWEPT_POINT_SETUP",
                &format!(
                    "所属条件の前に『xy平面上の任意の点を{}(x,y)とする』と定義してください",
                    point_name
                ),
            ));
        }
        let has_interpolation_range = ["満たす実数", "となる実数"].iter().any(|phrase| {
            latex.find(phrase).is_some_and(|position| {
                let before = &latex[..position];
                before.rfind("\\begin{gathered}").is_some_and(|start| {
                    compact_latex_structure(&before[start..]).contains("0\\leqqt\\leqq1")
                })
            })
        });
        let has_existence_sentence =
            latex.contains("実数") && latex.contains("存在する");
        if !has_interpolation_range || !has_existence_sentence {
            warnings.push(trajectory_warning(
                "TRAJECTORY_SWEPT_PARAMETER_CONDITION",
                "線分上の条件は補間パラメータ0\\leqq t\\leqq1を含む連立条件で表し、その条件を満たす実数パラメータが存在することを明記してください",
            ));
        }
        if has_existence_sentence
            && !(system_existence_condition_is_fully_quoted(latex)
                && latex.contains("\\text{「}")
                && latex.contains("\\text{を満たす実数 ")
                && latex.contains("が存在する」"))
        {
            warnings.push(trajectory_warning(
                "TRAJECTORY_SWEPT_QUOTED_CONDITION",
                "動く線分の所属条件では、開き鉤括弧を左波括弧より前へ、閉じ鉤括弧を存在文の末尾へ置き、連立式と存在文の全体を1組の「」で囲んでください",
            ));
        }

        let variation_table_start = [
            latex.find("\\begin{array}"),
            latex.find("\\begin{tabular}"),
        ]
        .into_iter()
        .flatten()
        .min();
        let existence_blocks = gathered_existence_blocks(latex);
        let has_multiple_parameter_condition = existence_blocks
            .iter()
            .any(|(_, block)| existence_variable_count(block).is_some_and(|count| count >= 2));
        if compact.contains("0\\leqqt\\leqq1")
            && latex.contains("値域")
            && has_multiple_parameter_condition
            && variation_table_start.is_some()
        {
            let table_start = variation_table_start.unwrap_or(latex.len());
            let multiple_before_table = existence_blocks
                .iter()
                .filter(|(position, block)| {
                    *position < table_start
                        && existence_variable_count(block).is_some_and(|count| count >= 2)
                })
                .count();
            let solved_interpolation_parameter_before_table = existence_blocks
                .iter()
                .any(|(position, block)| {
                    *position < table_start
                        && existence_variable_count(block).is_some_and(|count| count >= 2)
                        && {
                            let compact_block = compact_latex_structure(block);
                            compact_block.contains("t&=") || compact_block.contains("t=")
                        }
                });
            let single_parameter_before_table = existence_blocks.iter().any(|(position, block)| {
                *position < table_start && existence_variable_count(block) == Some(1)
            });
            let single_parameter_after_table = existence_blocks.iter().any(|(position, block)| {
                *position > table_start && existence_variable_count(block) == Some(1)
            });
            let membership_before_table =
                compact_latex_structure(&latex[..table_start]).matches(&membership).count();
            let membership_after_table =
                compact_latex_structure(&latex[table_start..]).matches(&membership).count();

            if multiple_before_table < 2
                || !solved_interpolation_parameter_before_table
                || !single_parameter_before_table
                || !single_parameter_after_table
                || membership_before_table < 2
                || membership_after_table < 1
            {
                warnings.push(trajectory_warning(
                    "TRAJECTORY_PARAMETER_ELIMINATION_FLOW",
                    "複数パラメータを含む所属条件では、所属条件を起点に補間パラメータを表す連立条件へ同値変形し、存在変数を1つに減らした条件を増減表の前へ置いてください。増減表の後は、その1変数の存在条件から値域へ同値変形して残るパラメータを消去してください",
                ));
            }
        }
    }

    if is_moving_figure_region_problem(problem_context) {
        if let Some(point_name) = point_membership_name(&compact) {
            let actual_discriminant_conflict =
                defined_region_symbol == Some('D') && latex.contains("判別式");
            let region_symbol = if actual_discriminant_conflict {
                'R'
            } else {
                defined_region_symbol.unwrap_or('R')
            };
            if opening_assumes_swept_region_membership(latex, point_name, region_symbol) {
                warnings.push(trajectory_warning(
                    "TRAJECTORY_SWEPT_ASSUMED_MEMBERSHIP",
                    &format!(
                        "{}(x,y)\\in {}はこれから判定する条件なので、{}を領域内の点と仮定せず、xy平面上の任意の点として置いてください",
                        point_name, region_symbol, point_name
                    ),
                ));
            }
        }
    }
    if let Some(region_symbol) = defined_region_symbol {
        let actual_discriminant_conflict = region_symbol == 'D' && latex.contains("判別式");
        if !actual_discriminant_conflict && !compact.contains(region_symbol) {
            warnings.push(trajectory_warning(
                "TRAJECTORY_DEFINED_REGION_SYMBOL",
                &format!(
                    "問題文で定義された領域・軌跡の記号{}を、実際の記号衝突がない限り保持してください",
                    region_symbol
                ),
            ));
        }
    }

    if contains_latex_command(latex, "\\Delta") || latex.contains('Δ') {
        warnings.push(trajectory_warning(
            "TRAJECTORY_DISCRIMINANT_SYMBOL",
            "2次方程式の判別式には必ずDを使用し、\\Deltaは使用しないでください",
        ));
    }
    if latex.contains("判別式") {
        let uses_d = ["判別式D", "判別式をD", "D=", "D>", "D<", "D\\geqq", "D\\leqq"]
            .iter()
            .any(|pattern| compact.contains(pattern));
        if !uses_d {
            warnings.push(trajectory_warning(
                "TRAJECTORY_DISCRIMINANT_SYMBOL",
                "判別式を用いる場合は判別式をDと定義し、以後もDで統一してください",
            ));
        }
    }

    let posthoc_phrases = [
        "以上の準備のもとで",
        "以上を一続きの同値変形でまとめると",
        "以上を同値変形で表すと",
        "ここまでの結果をまとめると",
        "以上の議論をまとめると",
        "これを同値変形の形で書けば",
    ];
    if posthoc_phrases.iter().any(|phrase| latex.contains(phrase)) {
        warnings.push(trajectory_warning(
            "TRAJECTORY_POSTHOC_EQUIVALENCE",
            if membership_equivalence {
                "不要な接続文を挟まず、必要最小限の準備計算の直後から点の集合への所属で始まる同値変形を記述してください"
            } else {
                "『以上の準備のもとで』等の不要な接続文を挟まず、領域決定または最終計算へ直接進んでください"
            },
        ));
    }

    if membership_equivalence {
        let split_proof_phrases = [
            "まず必要条件を求める",
            "逆に",
            "必要性",
            "十分性",
            "十分性を確認",
            "この範囲は十分でもある",
            "この範囲の任意の",
            "実際に$t$を定めることができる",
            "実際にtを定めることができる",
            "この条件を満たす点が実際に存在することを示す",
        ];
        if split_proof_phrases.iter().any(|phrase| latex.contains(phrase)) {
            warnings.push(trajectory_warning(
                "TRAJECTORY_SPLIT_NECESSITY_SUFFICIENCY",
                "必要性と十分性を独立した段落へ分けず、存在条件を保持した一続きの同値変形で処理してください",
            ));
        }

        push_existence_layout_warnings(&mut warnings, latex);

        if strict_point_locus {
            let conclusion_start = ["したがって、求める軌跡", "したがって、求める領域"]
                .iter()
                .filter_map(|phrase| latex.find(phrase))
                .min();
            if let Some(start) = conclusion_start {
                let conclusion_and_after = &latex[start..];
                if ["すなわち", "言い換えると", "換言すれば"]
                    .iter()
                    .any(|phrase| conclusion_and_after.contains(phrase))
                {
                    warnings.push(trajectory_warning(
                        "TRAJECTORY_REDUNDANT_CONCLUSION",
                        "最終結論の後で『すなわち』『言い換えると』等により同じ内容を再掲しないでください",
                    ));
                }
                if let Some(sentence_end) = conclusion_and_after.find('。') {
                    let tail = &conclusion_and_after[sentence_end + '。'.len_utf8()..];
                    if [
                        "端点は",
                        "それぞれの端点",
                        "端点を除いた",
                        "判別式が0になる点",
                        "これは2本の半直線",
                    ]
                    .iter()
                    .any(|phrase| tail.contains(phrase))
                    {
                        warnings.push(trajectory_warning(
                            "TRAJECTORY_REDUNDANT_CONCLUSION",
                            "範囲条件で明らかな端点・除外点・図形を、結論後に重複して説明しないでください",
                        ));
                    }
                }
            }
        }
    }

    if is_compound_trajectory_region_problem(problem_context) {
        let final_target_present = if problem_context.contains("体積") {
            latex.contains("体積") || compact.contains("V=")
        } else if problem_context.contains("面積") {
            latex.contains("面積") || compact.contains("S=")
        } else if problem_context.contains("確率") {
            latex.contains("確率")
        } else if problem_context.contains("最大値") || problem_context.contains("最大・最小") {
            latex.contains("最大値")
        } else if problem_context.contains("最小値") {
            latex.contains("最小値")
        } else {
            true
        };
        if !final_target_present {
            warnings.push(trajectory_warning(
                "TRAJECTORY_COMPOUND_INCOMPLETE",
                "領域の決定を途中結果として整理した後、問題が最終的に求める面積・体積・確率・最大最小などの計算まで続けてください",
            ));
        }
    }

    warnings.extend(scan_condition_quote_structure(latex));

    warnings
}

fn has_braced_system_trailing_comma(compact: &str) -> bool {
    for (opening, closing, needs_aligned) in [
        ("\\left\\{", "\\right.", true),
        ("\\left\\{", "\\right\\}", true),
        ("\\begin{cases}", "\\end{cases}", false),
    ] {
        let mut rest = compact;
        while let Some(start) = rest.find(opening) {
            let after_opening = &rest[start + opening.len()..];
            let end = after_opening.find(closing).unwrap_or(after_opening.len());
            let block = &after_opening[..end];
            if (!needs_aligned || block.contains("\\begin{aligned}"))
                && (block.contains(",\\\\")
                    || block.contains(",\\end{aligned}")
                    || block.ends_with(','))
            {
                return true;
            }
            if end >= after_opening.len() {
                break;
            }
            rest = &after_opening[end + closing.len()..];
        }
    }
    false
}

/// 高校教材で意味が伝わりにくい記号や、指定された答案表記に反する記号を検出する。
pub fn scan_solution_notation(latex: &str) -> Vec<AiWarning> {
    let lower = latex.to_ascii_lowercase();
    let compact: String = lower.chars().filter(|ch| !ch.is_whitespace()).collect();
    let mut warnings = vec![];

    if latex.contains("臨界点")
        || latex.contains("臨界値")
        || lower.contains("critical point")
        || lower.contains("critical value")
    {
        warnings.push(AiWarning {
            code: "NON_HIGH_SCHOOL_CRITICAL_TERM".into(),
            severity: "error".into(),
            message: "『臨界点』『臨界値』『critical point』『critical value』は使わず、『導関数が0になる点』『最大値をとる点』など高校で一般的な表現へ直してください".into(),
        });
    }

    let discusses_derivative_range = (latex.contains("値域")
        || latex.contains("最大値")
        || latex.contains("最小値"))
        && (compact.contains("'(")
            || compact.contains("\\frac{d}{d")
            || compact.contains("\\dfrac{d}{d"));
    let has_derivative_sign_change = latex.contains("正から負")
        || latex.contains("負から正")
        || (compact.contains(">0") && compact.contains("<0"))
        || ((compact.contains("\\begin{array}") || compact.contains("\\begin{tabular}"))
            && compact.contains('+')
            && compact.contains('-'));
    if discusses_derivative_range && has_derivative_sign_change {
        let variation_table_body = ["array", "tabular"].iter().find_map(|environment| {
            let opening = format!("\\begin{{{environment}}}");
            let closing = format!("\\end{{{environment}}}");
            let mut search_start = 0;
            while let Some(relative_start) = latex[search_start..].find(&opening) {
                let start = search_start + relative_start;
                let Some(relative_end) = latex[start..].find(&closing) else {
                    break;
                };
                let end = start + relative_end + closing.len();
                let table = &latex[start..end];
                if table.contains("'(")
                    || table.contains("\\frac{d}{d")
                    || table.contains("\\dfrac{d}{d")
                    || table.contains("\\nearrow")
                    || table.contains("\\searrow")
                {
                    return Some(table);
                }
                search_start = end;
            }
            None
        });
        match variation_table_body {
            None => warnings.push(AiWarning {
                code: "MISSING_VARIATION_TABLE".into(),
                severity: "error".into(),
                message: "1変数関数の値域・最大最小を導関数の符号変化から求める場合は、定義域、導関数が0になる点とその位置、各区間での符号を確認した後に増減表を示してください".into(),
            }),
            Some(table) => {
                let compact_table: String =
                    table.chars().filter(|ch| !ch.is_whitespace()).collect();
                let has_derivative_row = compact_table.contains("'(")
                    || compact_table.contains("\\frac{d}{d")
                    || compact_table.contains("\\dfrac{d}{d");
                let has_signs = compact_table.contains('+')
                    && compact_table.contains('-')
                    && compact_table.contains('0');
                let has_arrows = compact_table.contains("\\nearrow")
                    && compact_table.contains("\\searrow");
                if !has_derivative_row || !has_signs || !has_arrows {
                    warnings.push(AiWarning {
                        code: "INCOMPLETE_VARIATION_TABLE".into(),
                        severity: "error".into(),
                        message: "増減表には変数、導関数の正負と0、関数値、増加・減少を示す矢印を入れてください".into(),
                    });
                }
            }
        }
    }

    let inverse_trig_names = ["arcsin", "arccos", "arctan", "arccot", "arcsec", "arccsc"];
    if inverse_trig_names.iter().any(|name| lower.contains(name)) {
        warnings.push(AiWarning {
            code: "OUT_OF_SCOPE_INVERSE_TRIG".into(),
            severity: "error".into(),
            message: "arcsin、arccos、arctan等は使わず、y=\\sin^{-1}x等と置いた上でx=\\sin y等へ戻して高校範囲で処理してください".into(),
        });
    }

    let direct_inverse_derivatives = [
        "\\frac{d}{dx}\\sin^{-1}",
        "\\dfrac{d}{dx}\\sin^{-1}",
        "\\frac{d}{dx}\\left(\\sin^{-1}",
        "\\dfrac{d}{dx}\\left(\\sin^{-1}",
        "\\frac{d}{dx}\\cos^{-1}",
        "\\dfrac{d}{dx}\\cos^{-1}",
        "\\frac{d}{dx}\\left(\\cos^{-1}",
        "\\dfrac{d}{dx}\\left(\\cos^{-1}",
        "\\frac{d}{dx}\\tan^{-1}",
        "\\dfrac{d}{dx}\\tan^{-1}",
        "\\frac{d}{dx}\\left(\\tan^{-1}",
        "\\dfrac{d}{dx}\\left(\\tan^{-1}",
    ];
    if direct_inverse_derivatives
        .iter()
        .any(|pattern| compact.contains(pattern))
    {
        warnings.push(AiWarning {
            code: "DIRECT_INVERSE_TRIG_DERIVATIVE".into(),
            severity: "error".into(),
            message: "\\sin^{-1}x等を直接微分せず、x=\\sin y等の関係へ戻してから微分してください".into(),
        });
    }

    let forbidden_inequalities = [
        "\\leq",
        "\\geq",
        "\\le",
        "\\ge",
        "\\leqslant",
        "\\geqslant",
    ];
    if forbidden_inequalities
        .iter()
        .any(|command| contains_latex_command(latex, command))
        || latex.contains('≤')
        || latex.contains('≥')
    {
        warnings.push(AiWarning {
            code: "INEQUALITY_SYMBOL_STYLE".into(),
            severity: "error".into(),
            message: "等号を含む不等号は\\leqqまたは\\geqqで表記してください".into(),
        });
    }

    if contains_latex_command(latex, "\\exists")
        || contains_latex_command(latex, "\\forall")
    {
        warnings.push(AiWarning {
            code: "QUANTIFIER_NOTATION_STYLE".into(),
            severity: "error".into(),
            message: "\\existsや\\forallは使わず、『条件を満たす実数tが存在する』など高校答案で一般的な日本語で表してください".into(),
        });
    }

    let answer_decorations = ["\\boxed", "\\fbox", "\\framebox"];
    if answer_decorations
        .iter()
        .any(|command| contains_latex_command(latex, command))
        || latex.contains("(答)")
        || latex.contains("（答）")
    {
        warnings.push(AiWarning {
            code: "ANSWER_DECORATION".into(),
            severity: "error".into(),
            message: "答えを枠で囲んだり『(答)』と付けたりせず、解答の最後にそのまま記述してください".into(),
        });
    }

    let bold_vector_commands = ["\\mathbf", "\\boldsymbol", "\\bm", "\\pmb"];
    if bold_vector_commands
        .iter()
        .any(|command| contains_latex_command(latex, command))
    {
        warnings.push(AiWarning {
            code: "VECTOR_NOTATION_STYLE".into(),
            severity: "error".into(),
            message: "ベクトルは太字コマンドを使わず、\\vec{a}や\\overrightarrow{AB}のように矢印付きで表してください".into(),
        });
    }

    let coordinate_compact: String = latex
        .chars()
        .filter(|ch| !ch.is_whitespace() && *ch != '&')
        .collect::<String>()
        .replace("\\left", "")
        .replace("\\right", "")
        .replace("\\bigl", "")
        .replace("\\bigr", "")
        .replace("\\Bigl", "")
        .replace("\\Bigr", "");
    let coordinate_chars: Vec<char> = coordinate_compact.chars().collect();
    let point_coordinate_equality = coordinate_chars.iter().enumerate().any(|(index, ch)| {
        if !ch.is_ascii_uppercase()
            || coordinate_chars.get(index + 1) != Some(&'=')
            || coordinate_chars.get(index + 2) != Some(&'(')
        {
            return false;
        }
        if index > 0 {
            let previous = coordinate_chars[index - 1];
            if previous.is_ascii_alphanumeric() || previous == '\\' || previous == '}' {
                return false;
            }
        }
        coordinate_chars[index + 3..]
            .iter()
            .take_while(|candidate| **candidate != ')')
            .any(|candidate| *candidate == ',')
    });
    if point_coordinate_equality {
        warnings.push(AiWarning {
            code: "POINT_COORDINATE_NOTATION".into(),
            severity: "error".into(),
            message: "点名と座標を等号で結ばず、『ABの中点をM(x,y)とする』『点A(1,2)』のように表記してください".into(),
        });
    }

    if has_braced_system_trailing_comma(&compact) {
        warnings.push(AiWarning {
            code: "BRACED_SYSTEM_COMMA".into(),
            severity: "error".into(),
            message: "左波括弧内で数式・条件を縦に並べる場合は、各行末のコンマを削除してください".into(),
        });
    }

    let formula_period_patterns = [
        "$.",
        r#"\)."#,
        r#"\]."#,
        ".$",
        r#".\)"#,
        r#".\]"#,
        r#".\\"#,
        r#".\end{align"#,
        r#".\end{equation"#,
        r#".\end{gather"#,
        r#"\end{align*}."#,
        r#"\end{align}."#,
        r#"\end{equation*}."#,
        r#"\end{equation}."#,
        r#"\end{gather*}."#,
        r#"\end{gather}."#,
    ];
    // \right. のピリオドは不可視の区切り記号であり、文末の句点ではない。
    let formula_period_compact = compact.replace("\\right.", "\\right");
    if formula_period_patterns
        .iter()
        .any(|pattern| formula_period_compact.contains(pattern))
    {
        warnings.push(AiWarning {
            code: "FORMULA_TRAILING_PERIOD".into(),
            severity: "error".into(),
            message: "数式の末尾または数式を閉じた直後にASCIIのピリオドを付けないでください".into(),
        });
    }

    let checks: &[(&str, &[&str], &[&str], &str)] = &[
        (
            "合同・mod記号",
            &["\\pmod", "\\bmod", "\\mod", "\\equiv"],
            &["で割った余り", "余りが等しい", "法", "合同", "恒等的", "恒等式"],
            "modや合同記号を使う場合は『何で割った余りがどうなるか』を初出で説明してください",
        ),
        (
            "max記号",
            &["\\max", "operatorname{max}", "max\\{", "max{"],
            &["のうち大きい方", "大きい方を表", "最大のものを表"],
            "max記号を使う場合は『与えた値のうち大きい方』と初出で説明してください",
        ),
        (
            "min記号",
            &["\\min", "operatorname{min}", "min\\{", "min{"],
            &["のうち小さい方", "小さい方を表", "最小のものを表"],
            "min記号を使う場合は『与えた値のうち小さい方』と初出で説明してください",
        ),
        (
            "割り切れる記号",
            &["\\mid", "\\nmid"],
            &["で割り切れる", "約数である", "倍数である", "を満たす", "条件を表す"],
            "\\midや\\nmidを使う場合は、割り切れる関係または集合条件の意味を初出で説明してください",
        ),
        (
            "最大公約数・最小公倍数記号",
            &["\\gcd", "operatorname{gcd}", "operatorname{lcm}", "\\lcm"],
            &["最大公約数", "最小公倍数"],
            "gcdやlcmを使う場合は、それぞれ最大公約数・最小公倍数を表すことを説明してください",
        ),
        (
            "床・天井記号",
            &["\\lfloor", "\\rfloor", "\\lceil", "\\rceil"],
            &["以下の最大の整数", "以上の最小の整数", "整数部分", "床関数", "天井関数"],
            "床・天井記号を使う場合は『以下の最大の整数』『以上の最小の整数』などの意味を説明してください",
        ),
        (
            "符号関数記号",
            &["operatorname{sgn}", "\\sgn"],
            &["符号を表", "正のとき", "負のとき"],
            "sgnを使う場合は、正・0・負の各場合に何を表すか説明してください",
        ),
        (
            "argmax・argmin記号",
            &["argmax", "argmin", "arg\\,max", "arg\\,min"],
            &["最大にする", "最小にする", "最大値を与える", "最小値を与える"],
            "argmaxやargminを使う場合は『最大・最小にする変数の値』を表すことを説明してください",
        ),
    ];
    for (name, patterns, explanations, message) in checks {
        let used = patterns.iter().any(|pattern| lower.contains(pattern));
        let explained = explanations.iter().any(|phrase| latex.contains(phrase));
        if used && !explained {
            warnings.push(AiWarning {
                code: "UNEXPLAINED_NOTATION".into(),
                severity: "error".into(),
                message: format!("{}: {}", name, message),
            });
        }
    }
    warnings
}

